# Definir variáveis
x = 5
y = 20

# operação basica
soma = x + y
print(soma)

# operação mais avançada
resultado = x + (y * 5) % 2
print(resultado)

# int -> str
ano = str(2021)
print(type(ano))
print(ano)

# str -> int
age = int('21')
print(type(age))
print(age)

# str -> float
versao = float('3.6')
print(type(versao))
print(versao)

# Verificar o tipo da variável
tipo = type(x)
print(tipo)

# str
name = 'variables'
print(name)
