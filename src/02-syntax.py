# Isso é um comentário

# Declaração da variável x com o valor 5
x = 5

# Indentação
# é o espaço no inicio da linha para representar o bloco.

if 10 >= x:
  # A Indentação é importante no código
  # ela determina o bloco da instrução
  # no caso print faz parte do bloco do if 
  print("10 é maior ou igual a x") # O comentário pode ir depois da instrução 

else:
  print("10 não é menor que x")